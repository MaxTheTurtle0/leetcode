# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
def middleNode(head: Optional[ListNode]) -> Optional[ListNode]:
    fast = head
    while fast and fast.next:
        fast = fast.next.next
        head = head.next
    return head
